/**
 * Parse integer with fallback
 */
export const parseIntSafe = (value: string | number, fallback: number = 0): number => {
  if (typeof value === 'number') return Math.floor(value);
  const parsed = parseInt(value, 10);
  return isValidNumber(parsed) ? parsed : fallback;
};

/**
 * Parse float with fallback
 */
export const parseFloatSafe = (value: string | number, fallback: number = 0): number => {
  if (typeof value === 'number') return value;
  const parsed = parseFloat(value);
  return isValidNumber(parsed) ? parsed : fallback;
};

/**
 * Create a promise that resolves after specified time
 */
export const delay = (ms: number): Promise<void> => {
  return new Promise(resolve => setTimeout(resolve, ms));
};

/**
 * Create a promise that rejects after specified time
 */
export const timeout = <T>(promise: Promise<T>, ms: number): Promise<T> => {
  return Promise.race([
    promise,
    new Promise<never>((_, reject) =>
      setTimeout(() => reject(new Error('Operation timed out')), ms)
    )
  ]);
};

/**
 * Mask sensitive data for logging
 */
export const maskSensitiveData = (data: any): any => {
  if (typeof data !== 'object' || data === null) {
    return data;
  }

  const sensitiveKeys = ['password', 'token', 'secret', 'key', 'authorization'];
  const masked = { ...data };

  for (const key in masked) {
    if (sensitiveKeys.some(sensitive => key.toLowerCase().includes(sensitive))) {
      masked[key] = '***MASKED***';
    } else if (typeof masked[key] === 'object') {
      masked[key] = maskSensitiveData(masked[key]);
    }
  }

  return masked;
};